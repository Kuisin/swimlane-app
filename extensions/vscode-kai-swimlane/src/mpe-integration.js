const fs = require("node:fs");
const path = require("node:path");
const os = require("node:os");

const INTEGRATION_MARKER = "kai-swimlane-mpe-integration";

function getCrossnoteConfigDir() {
  const configured = process.env.MPE_CONFIG_PATH;
  if (configured) {
    return configured;
  }
  const xdg = process.env.XDG_CONFIG_HOME;
  if (xdg) {
    return path.join(xdg, ".crossnote");
  }
  return path.join(os.homedir(), ".crossnote");
}

function getThemeKey() {
  const vscode = require("vscode");
  return vscode.workspace.getConfiguration("kaiSwimlane").get("theme", "basic");
}

function buildParserJs(extensionPath, themeKey) {
  const rendererPath = path
    .join(extensionPath, "dist", "html-enhance.cjs")
    .replace(/\\/g, "/");

  return `// ${INTEGRATION_MARKER} — auto-generated by Kai Swimlane
const kaiSwimlane = require(${JSON.stringify(rendererPath)});

const KAI_SWIMLANE_THEME = ${JSON.stringify(themeKey)};

module.exports = {
  "${INTEGRATION_MARKER}": true,
  onWillParseMarkdown: async function (markdown) {
    return kaiSwimlane.expandFencesInMarkdown(markdown, {
      theme: KAI_SWIMLANE_THEME,
    });
  },
  onDidParseMarkdown: async function (html) {
    return kaiSwimlane.enhanceHtml(html, { theme: KAI_SWIMLANE_THEME });
  },
};
`;
}

/**
 * @param {import("vscode").ExtensionContext} context
 */
async function setupMarkdownPreviewEnhanced(context) {
  const vscode = require("vscode");
  const configDir = getCrossnoteConfigDir();
  const parserPath = path.join(configDir, "parser.js");

  fs.mkdirSync(configDir, { recursive: true });

  if (fs.existsSync(parserPath)) {
    const existing = fs.readFileSync(parserPath, "utf8");
    if (existing.trim() && !existing.includes(INTEGRATION_MARKER)) {
      const overwrite = await vscode.window.showWarningMessage(
        "parser.js already exists with custom hooks. Replace it with the Kai Swimlane integration?",
        { modal: true },
        "Replace",
        "Cancel"
      );
      if (overwrite !== "Replace") {
        return;
      }
    }
  }

  const themeKey = getThemeKey();
  const nextContent = buildParserJs(context.extensionPath, themeKey);
  fs.writeFileSync(parserPath, nextContent, "utf8");

  const mpeId = "shd101wyy.markdown-preview-enhanced";
  const hasMpe = vscode.extensions.getExtension(mpeId);

  const openParser = "Markdown Preview Enhanced: Extend Parser (Global)";
  await vscode.window.showInformationMessage(
    hasMpe
      ? "Kai Swimlane is wired into Markdown Preview Enhanced (parser.js). Reload the preview or export PDF again."
      : "Wrote parser.js for Markdown Preview Enhanced. Install the “Markdown Preview Enhanced” extension, then open its preview or export PDF.",
    hasMpe ? "Reload Window" : "OK"
  ).then((choice) => {
    if (choice === "Reload Window") {
      vscode.commands.executeCommand("workbench.action.reloadWindow");
    }
  });

  if (hasMpe) {
    try {
      await vscode.commands.executeCommand(
        "markdown-preview-enhanced.reloadPreview"
      );
    } catch {
      // Older MPE builds may not expose this command.
    }
  }
}

/**
 * @param {import("vscode").ExtensionContext} context
 */
function syncMpeParserTheme(context) {
  const configDir = getCrossnoteConfigDir();
  const parserPath = path.join(configDir, "parser.js");
  if (!fs.existsSync(parserPath)) {
    return;
  }
  const current = fs.readFileSync(parserPath, "utf8");
  if (!current.includes(INTEGRATION_MARKER)) {
    return;
  }
  const themeKey = getThemeKey();
  const next = buildParserJs(context.extensionPath, themeKey);
  fs.writeFileSync(parserPath, next, "utf8");
}

module.exports = {
  setupMarkdownPreviewEnhanced,
  syncMpeParserTheme,
  getCrossnoteConfigDir,
};
